public class LeapYear {
	
	int year;
	
	// divide by 4
	public static boolean divideBy4(int year){
		if( (year%4) == 0) {
			return true;
		} else 
			
		{ return false;
		
		}
		
	}
	
	
	public static boolean hundredandfourhundred(int year){
		if( (year%100) == 0 && (year%400) == 0) {
			return true;
		} else 
			
		{ return false;
		
		}
		
	}
	
		
	}
